const mongoose = require("mongoose");
const buyerSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        trim: true,
    },
    password: {
        type: String,
        required: true,
    },
});
module.exports = mongoose.model('Buyer', buyerSchema);